package Switch;

public class SwitchC {
    public void switchcla(){
        int day = 2;
        //Switch Case should always be small
        switch(day){
            case 1:
                System.out.println("Sunday");
                break;
            case 2:
                System.out.println("Monday");
            break;
            default:
                System.out.println("Hello");
        }

    }
}
