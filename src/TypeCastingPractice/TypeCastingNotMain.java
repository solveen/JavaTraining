package TypeCastingPractice;

public class TypeCastingNotMain {
    public void wideningCasting(){
        int a = 8;
        float myfloat = a;
        System.out.println(myfloat);
    }
    public void narrowingCasting(){
       float mf = 3.1f;
       int myint = (int)mf;
        System.out.println(myint);

    }
}
